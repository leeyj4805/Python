#숫자처리 함수 
#-abs 절대값이 나옴
# pow ~승 
# max 최대값이 나옴
# min 최소값이 나옴
# floor 내림
# ceil 올림
# sqrt 제곱근

print(abs(-5)) # 5
print(pow(4, 2)) # 4^2 = 4*4 = 16
print(max(5, 12)) # 12 
print(min(5, 12)) # 5
print(round(3.14)) # 3
print(round(4.99)) # 5

from math import * 
print(floor(4.99)) # 내림 4
print(ceil(3.14)) # 올림 4
print(sqrt(16)) # 제곱근 4
